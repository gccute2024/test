//----------------------------------------------------------------------------
// Set&Get Status for Next Port60 Data Handle
//----------------------------------------------------------------------------
VBYTE Set_PortPM5_Data_Handle(void)
{
    if( PM5Step == 0x00 )
    {
        PM5Step=0x01;   // Set Next Port60 Data Handle again
        return(0);
    }
    else
    {
        PM5Step=0x00;   // Set Command Finished
        return(1);
    }

}

void PMC5_Cmd_33(void)
{
    if( Set_PortPM5_Data_Handle() )  //Get PM5Data
    {
        PMC5_DOR = 0x00;
    }
}
