void Service_PCI6_Main(void)
{
    if( PMC5_STR & C_D5 )
    {
        PM5Cmd  = PMC5_DIR;
        PM5Step = 0;

	        dprint("PMC5_STR is 0x%x,PM5Cmd is 0x%x 0x1419 is 0x%x ,0x1420 is 0x%x\n",PMC5_STR,PM5Cmd,(*(BYTE *)(0x1419)),(*(BYTE *)(1420)));

        (Port7C_Table[PM5Cmd>>4])();
    }
    else
    {
        PM5Data = PMC5_DIR;

	        dprint("PMC5_STR is 0x%x,PM5Data is 0x%x 0x1419 is 0x%x ,0x1420 is 0x%x\n",PMC5_STR,PM5Data,(*(BYTE *)(0x1419)),(*(BYTE *)(1420)));

        if ( PM5Step != 0x00 )
        {
             // Pls use PM5Step for Port 7C Routine Command
            (Port7C_Table[PM5Cmd>>4]());
        }
        else
        {
            PM5UnProcessCnt=0x0;
            while( PM5UnProcessCnt < 0xFF )
            {
                PM5UnProcessCnt++;
            }
        }
    }
}

