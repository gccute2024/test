void Service_PCI5_Main(void)
{
    if( PMC4_STR & C_D4 )
    {
        PM4Cmd  = PMC4_DIR;
	        dprint("PM4Cmd is 0x%x\n",PM4Cmd);

        PM4Step = 0;
        (Port78_Table[PM4Cmd>>4])();
    }
    else
    {
        PM4Data = PMC4_DIR;
        PM4Step = 0;

	        dprint("PM4Data is 0x%x\n",PM4Data);
        
        if( PM4Step != 0x00 )
        {
             // Pls use PM4Step for Port 78 Routine Command
            (Port78_Table[PM4Cmd>>4]());
        }
        else
        {
            PM4UnProcessCnt=0;
            while( PM4UnProcessCnt < 0xFF )
            {
                PM4UnProcessCnt++;
            }
        }
    }
}

