void Service_PCI3_Main(void)
{
    VBYTE cnt=0;
    #if ENABLE_DEBUGGER_SUPPORT
	    /* Debuffer record */
	    Debugger_KBC_PMC_Record(0,2,PMC2_DIR);
    #endif

    if( PMC2_STR & C_D2 )
    {
        PM2Cmd  = PMC2_DIR;

	if(PM2Cmd != 0x0)
		    dprint("Enter PMC2_STR & C_D2 PM2Cmd is 0x%x,PMC2_STR is 0x%x \n",PM2Cmd,PMC2_STR);

        PM2Step = 0;
        (Port6C_Table[PM2Cmd>>4])();
    }
    else
    {
        PM2Data = PMC2_DIR;

	    if(PM2Data != 0x0)
		        dprint("Enter else PM2Data is 0x%x, PMC2_STR is 0x%x \n", PM2Data,PMC2_STR);
        if( PM2Step != 0x00 )
        {
            // Pls use PM2Step for Port 6C Routine Command
            (Port6C_Table[PM2Cmd>>4]());
        }
        else
        {
            while( cnt < 0xFF )
            {
                cnt++;
            }
        }

    }
}

