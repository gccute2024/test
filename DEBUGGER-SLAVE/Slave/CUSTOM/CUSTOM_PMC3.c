void Service_PCI4_Main(void)
{ 
    VBYTE cnt=0;
        dprint("Service_PCI4_Main \n");

    if( PMC3_STR & C_D3 )
    {
        PM3Cmd  = PMC3_DIR;
	        dprint("PM3Cmd is 0x%x\n",PM3Cmd);
        PM3Step = 0;
        (Port6E_Table[PM3Cmd>>4])();
    }
    else
    {
        PM3Data = PMC3_DIR;
	        dprint("PM3Data is 0x%x\n",PM3Data);
        if( PM3Step != 0x00 )
        {
             // Pls use PM3Step for Port 6E Routine Command
            (Port6E_Table[PM3Cmd>>4]());
        }
        else
        {
            while( cnt < 0xFF )
            {
                cnt++;
            }
        }

    }

    #if ENABLE_DEBUGGER_SUPPORT
	    /* Debugger record */
	    Debugger_KBC_PMC_Record(0,3,PMC3_DIR);
    #endif
}

