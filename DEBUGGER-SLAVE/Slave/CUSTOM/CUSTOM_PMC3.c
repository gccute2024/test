//----------------------------------------------------------------------------
// Set&Get Status for Next Port60 Data Handle
//----------------------------------------------------------------------------
VBYTE Set_PortPM3_Data_Handle(void)
{
    if ( PM3Step == 0x00 )
    {
        PM3Step=0x01;   // Set Next Port60 Data Handle again
        return(0);
    }
    else
    {
        PM3Step=0x00;   // Set Command Finished
        return(1);
    }

}

void PMC3_Cmd_33(void)
{
    if ( Set_PortPM3_Data_Handle() )  //Get PM3Data
    {
        PMC3_DOR = 0x00;
    }
}
