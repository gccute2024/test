//----------------------------------------------------------------------------
// Special Handle
// Get 1 Bytes Embedded Controller Data, EC_DATA (R/W)
//----------------------------------------------------------------------------
BYTE OEM_Get_Port62_Data(void)
{

    WORD  iLOOP;
    iLOOP = WaitECDataDelay;

    do
    {
        if ( IS_SET(PMC1_STR,1) )
        {
            if ( PMC1_STR & KBC_STA_A2 )
            {
                return 0x00;
            }
            else
            {
                PM1Data = PMC1_DIR;            // Load data
                return 0x01;
            }
        }

        iLOOP--;
    }
    while (iLOOP != 0 );

    return 0x00;

}
